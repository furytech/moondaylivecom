import { serve } from "https://deno.land/std@0.190.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { birthMoonSign, currentMoonSign, currentPhase } = await req.json();

    if (!birthMoonSign || !currentMoonSign) {
      return new Response(JSON.stringify({ error: "Missing moon data" }), {
        status: 400,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY missing");

    const systemPrompt = `You are the Moonday Live oracle — luxurious, poetic, brief. Voice is sovereign, mystical, intimate. Never clinical. Entertainment only — never give medical, financial, or legal advice. Never use the word "horoscope" or "prediction". Use "lunar signature", "Between Phases", "Mind / Soul / Body" if relevant.`;

    const userPrompt = `Generate a TEASER preview (max 55 words) of the Sovereign reading for a member whose natal moon is in ${birthMoonSign} during the current ${currentPhase ?? ""} moon in ${currentMoonSign}.

Structure: one evocative opening line naming the ${birthMoonSign} × ${currentMoonSign} dynamic, then ONE specific Mind/Soul/Body insight, then trail off mid-revelation with an ellipsis to invite unlocking the full reading. Do NOT include a CTA. Do NOT mention subscribing. Just the mystical teaser text.`;

    const resp = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          { role: "user", content: userPrompt },
        ],
      }),
    });

    if (!resp.ok) {
      if (resp.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit. Please try again shortly." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (resp.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await resp.text();
      console.error("AI gateway error", resp.status, t);
      return new Response(JSON.stringify({ error: "AI gateway error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    const data = await resp.json();
    const teaser = data?.choices?.[0]?.message?.content ?? "";

    return new Response(JSON.stringify({ teaser }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
      status: 200,
    });
  } catch (e) {
    const msg = e instanceof Error ? e.message : String(e);
    console.error("[sovereign-teaser] error", msg);
    return new Response(JSON.stringify({ error: msg }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
